#ifndef APP_CONFIG_H
#define APP_CONFIG_H

#define VOLTS_TO_ADC(v) ((v)*1023 * 3 / (13 * 2.56))

#define TIMER1_PRESCALER (8)
#define TIMER0_PRESCALER (64)

#define DYNAMO_FREQ_TO_CTR(f) ((F_CPU/(f*TIMER1_PRESCALER)))
#define US_TO_CTR(us) ((F_CPU/(1000000*TIMER0_PRESCALER))*us)

#define CHARGE_VOLTAGE_STEP_TIME (US_TO_CTR(2000))

#define MAX_DISCHARGE_VALUE (VOLTS_TO_ADC(3.5))
#define MIN_CHARGE_VALUE (VOLTS_TO_ADC(2.5))
#define MAX_CHARGE_VALUE (VOLTS_TO_ADC(9.2))
#define MIN_CHARGE_DISCHARGE_SPAN (VOLTS_TO_ADC(0.5))
#define CHARGE_VOLTAGE_STEP (VOLTS_TO_ADC(0.1))
#define DYNAMO_FREQUENCY_STEP_TIMING (1)
#define DYNAMO_FREQUENCY_DANGER_VOLTAGE (DYNAMO_FREQ_TO_CTR(480))

#endif